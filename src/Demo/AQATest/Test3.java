package Demo.AQATest;

public class Test3 {
    private int age;
    private int price;
    private int salary;

    public Test3(int age, int price, int salary) {
        this.age = age;
        this.price = price;
        this.salary = salary;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }
}
