package StarterLevel.L2Operathions;

public class C3_ArithmeticOperation {


    public static void main(String[] args) {

        byte sum1 = 5, sum2 = 7;
        int res1 = sum1 + sum2;

        System.out.println(res1);

        int mines1 = 9, mines2 = 5;
        int res2 = mines1 - mines2;

        System.out.println(res2);

        int multiplication1 = 5, multiplication2 = 5;
        int res3 = 0;
        res3 = multiplication1 * multiplication2;

        System.out.println(res3);


        double div1 = 8, div2 = 2, div3 = 3;
        double res4 = div1 / div2;

        System.out.println(res4);

        double res5 = div1 % div3;

        System.out.println(res5);
    }
}
