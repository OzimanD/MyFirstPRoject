package StarterLevel.L2Operathions;

public class C6_Comparison {
    public static void main(String[] args) {
        byte val1 = 0, val2 = 1;
        boolean res;

        res = val1 < val2;
        System.out.println(res);

        res = val1 > val2;
        System.out.println(res);

        res = val1 <= val2;
        System.out.println(res);

        res = val1 >= val2;
        System.out.println(res);

        res = val1 == val2;
        System.out.println(res);

        res = val1 != val2;
        System.out.println(res);
    }

}
